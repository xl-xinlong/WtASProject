package com.woting.activity.home.program.tuijian.slideshowview.model;

public class ImgReceive {
	private String ImgUrl;
	private String ImgDesc;
	private String ImgIdx;
	private String ImgContentUrl;

	public String getImgUrl() {
		return ImgUrl;
	}

	public void setImgUrl(String imgUrl) {
		ImgUrl = imgUrl;
	}

	public String getImgDesc() {
		return ImgDesc;
	}

	public void setImgDesc(String imgDesc) {
		ImgDesc = imgDesc;
	}

	public String getImgIdx() {
		return ImgIdx;
	}

	public void setImgIdx(String imgIdx) {
		ImgIdx = imgIdx;
	}

	public String getImgContentUrl() {
		return ImgContentUrl;
	}

	public void setImgContentUrl(String imgContentUrl) {
		ImgContentUrl = imgContentUrl;
	}
}
